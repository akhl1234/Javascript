let x = 5;
let y = -10;
let z = "Hello-G";

console.log("Unary Plus (+):", +x); // Output: 5
console.log("Unary Minus (-):", -y); // Output: 10

// Increment (++) and Decrement (--)
console.log("Increment (++x):", ++x); // Output: 6 (x is now 6)
console.log("Decrement (--y):", --y); // Output: -11 (y is now -11)

// typeof Operator
console.log("Type of x:", typeof x); // Output: number
console.log("Type of z:", typeof z); // Output: string
