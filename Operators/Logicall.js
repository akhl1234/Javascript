// Logical AND (&&)
let x = 5;
let y = 10;
let resultAND = x < 10 && y > 5; // true because both conditions are true

// Logical OR (||)
let z = 15;
let resultOR = x < 10 || z < 10; // true because at least one condition is true

// Logical NOT (!)
let isFalse = false;
let resultNOT = !isFalse; // true because the operand is false, and ! flips it to true

console.log("Logical AND:", resultAND);
console.log("Logical OR:", resultOR);
console.log("Logical NOT:", resultNOT);
