// Data types describe the different types or kinds of data that we're gonna be working with and storing in variables. In Javascript, there are five basic, or primitive, types of data. The five most basic types of data are strings, numbers, booleans, undefined, and null.

// String data type
var name = 'John';

// Number data type
var age = 25;

// Boolean data type
var isStudent = true;

// Array data type
var fruits = ['apple', 'banana', 'orange'];

// Object data type
var person = {
  name: 'John',
  age: 25,
  isStudent: true
};

// Print the values and types of the variables to the console
console.log('Name:', name, typeof name);
console.log('Age:', age, typeof age);
console.log('Is Student:', isStudent, typeof isStudent);
console.log('Fruits:', fruits, typeof fruits);
console.log('Person:', person, typeof person);
